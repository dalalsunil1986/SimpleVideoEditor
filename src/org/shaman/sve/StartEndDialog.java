	public static final int RET_CANCEL = 0;
	/**
	 * A return status code - returned if OK button has been pressed
	 */
	public static final int RET_OK = 1;

	public static boolean showDialog(java.awt.Frame parent, Project project, FrameTime outStart, FrameTime outEnd) {
		StartEndDialog d = new StartEndDialog(parent, true, project);
		d.pack();
		d.setLocationRelativeTo(null);
		d.setVisible(true);
		if (d.getReturnStatus() == RET_OK) {
			outStart.set(d.start);
			outEnd.set(d.end);
			return true;
		} else {
			return false;
		}
	}
	
