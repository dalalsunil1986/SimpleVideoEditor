    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
		if (startA.isSelected()) {
			start = new FrameTime(project.getFramerate());
		} else {
			start = project.getMarkerA();
		}
		if (endA.isSelected()) {
			end = project.getLength();
		} else {
			end = project.getMarkerB();
		}
		doClose(RET_OK);
    }//GEN-LAST:event_okButtonActionPerformed

